def code_list(items: list[str]) -> str:
    return "<br>".join(f"`{item}`" for item in items)


def unique(items: list[str]) -> list[str]:
    return list(dict.fromkeys(items))


def has_reproduced_relationship(result: dict, relationships: set[str]) -> bool:
    return any(
        record["reproduced"] and record["relationship"] in relationships
        for record in result["formalizations"]
    )


def strongest_relationship(result: dict) -> str:
    reproduced = [
        record["relationship"]
        for record in result["formalizations"]
        if record["reproduced"]
    ]
    coverage = [r for r in reproduced if r in COVERAGE_RELATIONSHIPS]
    if coverage:
        return code_list(unique(coverage))
    if reproduced:
        return code_list(unique(reproduced))
    return "—"


def render_atlas_index(registry: dict) -> str:
    """One human-browsable row per surveyed result, generated from the registry so
    the full 44-row landscape is readable without parsing registry.yaml."""
    results = registry["results"]
    lines = [
        "<!-- Generated by scripts/generate_registry_views.py; do not edit directly. -->",
        "# Atlas Index",
        "",
        "One row per surveyed impossibility result, generated from",
        "[`registry.yaml`](../../registry.yaml). `Formalization` is the strongest",
        "reproduced relationship; `Bridge` is the AI-system interpretation review",
        "state; `Candidate` counts manually discovered leads not yet accepted as",
        "coverage. See [formalization status](formalization-status.md) for headline",
        "coverage counting and [open work](../guide/open-work.md) for next actions.",
        "",
        "| ID | Survey result | Informal claim | Formalization | Bridge | Candidate leads |",
        "|---|---|---|---|---|---:|",
    ]
    for result in results:
        claim = result["informal_claim"].replace("|", "\\|")
        leads = result.get("candidate_formalizations", [])
        candidate = str(len(leads)) if leads else "—"
        lines.append(
            f"| {result['id']} | {result['name']} | {claim} | "
            f"{strongest_relationship(result)} | `{result['ai_bridge_status']}` | {candidate} |"
        )
    lines.append("")
    return "\n".join(lines)


def render_status(registry: dict, search_evidence: dict) -> str:
    results = registry["results"]
    covered = [
        result
        for result in results
        if has_reproduced_relationship(result, COVERAGE_RELATIONSHIPS)
    ]
    related_only = [
        result
        for result in results
        if not has_reproduced_relationship(result, COVERAGE_RELATIONSHIPS)
        and has_reproduced_relationship(result, {"RELATED"})
    ]
    formalizations = [
        record for result in results for record in result["formalizations"]
    ]
    artifacts = [
        declaration
        for result in results
        if result["lean_artifact"] is not None
        for declaration in result["lean_artifact"]["declarations"]
    ]
    bridges = [artifact for artifact in artifacts if artifact["type"] == "BRIDGE"]
    wrappers = [artifact for artifact in artifacts if artifact["type"] == "WRAPPER"]
    new_proofs = [artifact for artifact in artifacts if artifact["type"] == "NEW_PROOF"]
    references = [artifact for artifact in artifacts if artifact["type"] == "REFERENCE"]
    rows_with_bridge = sum(
        result["lean_artifact"] is not None
        and any(d["type"] == "BRIDGE" for d in result["lean_artifact"]["declarations"])
        for result in results
    )
    rows_wrapper_only = sum(
        result["lean_artifact"] is not None
        and all(d["type"] == "WRAPPER" for d in result["lean_artifact"]["declarations"])
        for result in results
    )
    candidate_leads = sum(
        len(result.get("candidate_formalizations") or []) for result in results
    )
    external = [
        (result, record)
        for result in results
        for record in result["formalizations"]
        if record["framework"] != "Lean" and record["reproduced"]
    ]
    reviewed_results = sum(
        result["ai_bridge_status"] != "HUMAN_REVIEW" for result in results
    )
    candidate_results = sum(
        any(hit["hit_count"] for hit in evidence["candidate_hits"].values())
        for evidence in search_evidence["results"].values()
    )

    lines = [
        "<!-- Generated by scripts/generate_registry_views.py; do not edit directly. -->",
        "# Formalization Status",
        "",
        "The machine-readable sources of truth are [`registry.yaml`](../../registry.yaml)",
        "and [`formalization-search.json`](../provenance/formalization-search.json). Adjacent",
        "non-survey formalizations live in [`landscape.yaml`](../../landscape.yaml) and",
        "do not affect the headline coverage count. Regenerate this page with",
        "`python3 scripts/generate_registry_views.py`.",
        "",
        "| Metric | Current |",
        "|---|---:|",
        f"| Survey results inventoried | {len(results)} / {registry['survey']['expected_result_count']} |",
        f"| Survey results with verified `EXACT`/`EQUIVALENT` formalizations | {len(covered)} |",
        f"| Additional survey results with `RELATED` formalizations only | {len(related_only)} |",
        f"| Verified formalization records | {len(formalizations)} |",
        f"| Atlas Lean theorem declarations | {len(artifacts)} across {sum(result['lean_artifact'] is not None for result in results)} survey results |",
        f"| … of which `WRAPPER` | {len(wrappers)} |",
        f"| … of which `BRIDGE` | {len(bridges)} |",
        f"| … of which `NEW_PROOF` | {len(new_proofs)} |",
        f"| … of which `REFERENCE` | {len(references)} |",
        f"| Survey rows whose atlas Lean is wrapper-only | {rows_wrapper_only} |",
        f"| Survey rows with at least one `BRIDGE` declaration | {rows_with_bridge} |",
        f"| Structured `candidate_formalizations` leads | {candidate_leads} |",
        f"| Reproduced external coverage records | {len(external)} across {len({result['id'] for result, _ in external})} survey results |",
        f"| Survey results with reviewed AI-system bridges | {reviewed_results} |",
        f"| Survey rows with six-corpus discovery evidence | {len(search_evidence['results'])} / {len(results)} |",
        "",
        "Headline formalization coverage counts a survey result only when it has a",
        "reproduced `EXACT` or `EQUIVALENT` record. That is a **map** metric (evidence",
        "exists), not a **research-density** metric: many covered rows expose only",
        "`WRAPPER` aliases over maintained upstream mathematics. A `RELATED` bridge",
        "remains visible but does not increase the headline count, especially while",
        "its interpretation is under `HUMAN_REVIEW`.",
        "",
        "## Atlas Lean declarations",
        "",
        "| ID | Survey result | Formalization relationships | Source declarations | Atlas declarations and layers |",
        "|---|---|---|---|---|",
    ]

    for result in results:
        artifact = result["lean_artifact"]
        if artifact is None:
            continue
        declarations = artifact["declarations"]
        sources = unique(
            [
                source
                for declaration in declarations
                for source in declaration["source_declarations"]
            ]
        )
        atlas = [
            f"{declaration['atlas_declaration']} ({declaration['type']})"
            for declaration in declarations
        ]
        relationships = unique(
            [record["relationship"] for record in result["formalizations"]]
        )
        lines.append(
            f"| {result['id']} | {result['name']} | {code_list(relationships)} | "
            f"{code_list(sources)} | {code_list(atlas)} |"
        )

    lines.extend(
        [
            "",
            "The generated declaration-presence checks live in",
            "[`AISafetyAtlas/Examples/Registry.lean`](../../AISafetyAtlas/Examples/Registry.lean).",
            "The hand-written `PublicAPI` examples separately protect the intended theorem",
            "signatures and root-import usability.",
            "",
            "## Reproduced external coverage",
            "",
            "| ID | Survey result | Framework | Declaration | Relationship | Reproduction command |",
            "|---|---|---|---|---|---|",
        ]
    )
    for result, record in external:
        lines.append(
            f"| {result['id']} | {result['name']} | {record['framework']} | "
            f"`{record['declaration']}` | {record['relationship']} | "
            f"`{record['build_command']}` |"
        )

    lines.extend(
        [
            "",
            "Additional reproduced variants and adjacent developments, including vNM",
            "expected utility, remain provenance rather than extra survey coverage; see",
            "[`external-formalizations.md`](../provenance/external-formalizations.md) and the",
            "[landscape index](landscape-index.md).",
            "",
            "## Discovery coverage",
            "",
            f"All {len(results)} rows have a **baseline classical six-corpus** search",
            "(Mathlib, Isabelle AFP, Rocq Undecidability, HOL4, HOL Light, Agda stdlib);",
            f"{candidate_results} rows produced raw candidate-file hits. That pass is",
            "**scoped** negative/positive evidence for those corpora only — it does not",
            "search third-party Lean packages (Foundation, KolmogorovMathlib, SocialChoiceLean,",
            "DASH, …). Manually discovered package leads use `candidate_formalizations` or",
            "[`landscape.yaml`](../../landscape.yaml). Candidate hits are discovery evidence,",
            "not verified formalizations. Relationship labels are",
            "`EXACT`, `EQUIVALENT`, `RELATED`, `DEPENDENCY_ONLY`, and `UNCLEAR`.",
            "An AI-safety interpretation remains `HUMAN_REVIEW` unless it has been",
            "separately defined and reviewed.",
            "",
        ]
    )
    return "\n".join(lines)


def _md_cell(text: str) -> str:
    return text.replace("|", "\\|").replace("\n", " ")


def _short_citation(citation: str, limit: int = 120) -> str:
    citation = " ".join(citation.split())
    if len(citation) <= limit:
        return citation
    return citation[: limit - 1] + "…"


def render_paper_coverage(registry: dict, landscape: dict) -> str:
    """Human table: source papers ↔ formalizations ↔ atlas surface (R coverage view)."""
    catalog = registry["source_catalog"]
    lines = [
        "<!-- Generated by scripts/generate_registry_views.py; do not edit directly. -->",
        "# Paper and formalization coverage",
        "",
        "One place to see **which source papers** the survey maps, **whether a",
        "machine-checked formalization exists**, and **where it lives** in the atlas.",
        "Generated from [`registry.yaml`](../../registry.yaml) and",
        "[`landscape.yaml`](../../landscape.yaml). Headline coverage still means",
        "reproduced `EXACT`/`EQUIVALENT` only; `RELATED` and landscape rows are listed",
        "but do not increase that count. See also [formalization status](formalization-status.md)",
        "and [atlas index](atlas-index.md).",
        "",
        "## Survey Table 1 results",
        "",
        "| ID | Result | Source paper(s) | Formalization (framework · relationship · declaration) | Atlas Lean | Bridge |",
        "|---|---|---|---|---|---|",
    ]
    for result in registry["results"]:
        refs = result.get("original_source_refs") or []
        if refs:
            papers = "<br>".join(
                _md_cell(_short_citation(catalog[ref]["citation"]))
                for ref in refs
                if ref in catalog
            )
            if not papers:
                papers = "—"
        else:
            papers = "—"

        forms = result.get("formalizations") or []
        if forms:
            form_cells = []
            for record in forms:
                decl = record.get("declaration") or "—"
                form_cells.append(
                    f"{record['framework']} · `{record['relationship']}` · `{decl}`"
                    + (" · reproduced" if record.get("reproduced") else " · not reproduced")
                )
            formalization = "<br>".join(form_cells)
        else:
            formalization = "— (mapped only)"

        if result.get("lean_artifact"):
            atlas = code_list(
                d["atlas_declaration"]
                for d in result["lean_artifact"]["declarations"]
            )
        else:
            atlas = "—"

        lines.append(
            f"| {result['id']} | {_md_cell(result['name'])} | {papers} | "
            f"{formalization} | {atlas} | `{result['ai_bridge_status']}` |"
        )

    lines.extend(
        [
            "",
            "## Landscape (non–Table-1)",
            "",
            "These are first-class provenance or public Lean surface **outside** the",
            "headline 44-row coverage count.",
            "",
            "| ID | Name | Framework | Declaration / atlas | Reproduction | Notes |",
            "|---|---|---|---|---|---|",
        ]
    )
    for entry in landscape.get("entries", []):
        decl = entry.get("atlas_declaration") or entry.get("declaration") or "—"
        if decl != "—":
            decl = f"`{decl}`"
        repro = entry.get("reproduction") or "—"
        if repro != "—":
            repro = f"`{repro}`"
        notes = _md_cell(entry.get("notes") or "—")
        lines.append(
            f"| {entry['id']} | {_md_cell(entry['name'])} | {entry['framework']} | "
            f"{decl} | {repro} | {notes} |"
        )

    lines.extend(
        [
            "",
            "## AI safety literature (how addressed)",
            "",
            "Literature-first coverage estimation (Melo-style packaging, robot ethics,",
            "etc.) lives in the hand map",
            "[`related-literature.md`](../guide/related-literature.md)—not theorem-first.",
            "Some rows also appear as survey BY IDs above; do not double-count headline",
            "EXACT/EQUIVALENT. Optional `LAND-*` / `atlas-ref-*` keys support packaging.",
            "",
            "Regenerate: `python3 scripts/generate_registry_views.py`.",
            "",
        ]
    )
    return "\n".join(lines)


