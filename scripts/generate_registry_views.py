def render_state_snapshot(registry: dict, landscape: dict) -> str:
    """Generated block embedded in STATE.md so hand counts cannot drift (R7-1)."""
    results = registry["results"]
    covered = sum(
        has_reproduced_relationship(result, COVERAGE_RELATIONSHIPS) for result in results
    )
    related_only = sum(
        not has_reproduced_relationship(result, COVERAGE_RELATIONSHIPS)
        and has_reproduced_relationship(result, {"RELATED"})
        for result in results
    )
    artifacts = [
        declaration
        for result in results
        if result["lean_artifact"] is not None
        for declaration in result["lean_artifact"]["declarations"]
    ]
    lean_rows = sum(result["lean_artifact"] is not None for result in results)
    bridges = sum(a["type"] == "BRIDGE" for a in artifacts)
    wrappers = sum(a["type"] == "WRAPPER" for a in artifacts)
    reviewed = sum(result["ai_bridge_status"] != "HUMAN_REVIEW" for result in results)
    candidates = sum(
        len(result.get("candidate_formalizations") or []) for result in results
    )
    lines = [
        STATE_START,
        "<!-- Generated by scripts/generate_registry_views.py; do not edit by hand. -->",
        f"- Registry snapshot: **{len(artifacts)}** atlas Lean theorem declarations across",
        f"  **{lean_rows}** survey results; headline `EXACT`/`EQUIVALENT` coverage",
        f"  **{covered} of {len(results)}**; RELATED-only **{related_only}**; reviewed",
        f"  AI-system bridges **{reviewed}**; declaration layers WRAPPER **{wrappers}** /",
        f"  BRIDGE **{bridges}**; structured candidate leads **{candidates}**; landscape",
        f"  entries **{len(landscape.get('entries', []))}** (see `landscape.yaml`).",
        STATE_END,
    ]
    return "\n".join(lines)


def render_state(state: str, registry: dict, landscape: dict) -> str:
    generated = render_state_snapshot(registry, landscape)
    start = state.find(STATE_START)
    end = state.find(STATE_END)
    if start < 0 or end < start:
        # Insert snapshot after the phase line / first blank block if markers missing.
        raise ValueError(
            "STATE.md generated-snapshot markers are missing or out of order; "
            f"expected {STATE_START!r} … {STATE_END!r}"
        )
    end += len(STATE_END)
    return state[:start] + generated + state[end:]


def render_readme(readme: str, registry: dict) -> str:
    results = registry["results"]
    covered = sum(
        has_reproduced_relationship(result, COVERAGE_RELATIONSHIPS)
        for result in results
    )
    related_only = sum(
        not has_reproduced_relationship(result, COVERAGE_RELATIONSHIPS)
        and has_reproduced_relationship(result, {"RELATED"})
        for result in results
    )
    related_noun = "result" if related_only == 1 else "results"
    reviewed = sum(
        result["ai_bridge_status"] != "HUMAN_REVIEW" for result in results
    )
    generated = "\n".join(
        [
            README_START,
            f"The current registry has verified `EXACT` or `EQUIVALENT` formalization coverage for",
            f"**{covered} of {len(results)}** survey results. It records **{related_only} additional {related_noun} with a",
            "`RELATED` formalization only**, outside headline coverage, and",
            f"**{reviewed} survey results with reviewed AI-system bridges**. It provides",
            "infrastructure for further formalization; it does not claim complete formal",
            "coverage. See the current [formalization status](docs/status/formalization-status.md).",
            README_END,
        ]
    )
    start = readme.find(README_START)
    end = readme.find(README_END)
    if start < 0 or end < start:
        raise ValueError("README generated-scope markers are missing or out of order")
    end += len(README_END)
    return readme[:start] + generated + readme[end:]


